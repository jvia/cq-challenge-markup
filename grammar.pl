/********************************************************************/
/*                             Grammar                              */
/********************************************************************/

body([]) --> [eof] | [].



/********************************************************************/
/*                         XML Transformer                          */
/********************************************************************/

ast_to_xml([], "<body/>").
ast_to_xml(AST, XML) :-
        ast_to_xml(AST, "<body>", XML).


ast_to_xml([], Accm, XML) :-
        append(Accm, "</body.", XML).
